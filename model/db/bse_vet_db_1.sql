CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_control_mascota`(IN `ve_opcion` VARCHAR(50))
    NO SQL
BEGIN

IF ve_opcion = 'opc_contar' THEN
	SELECT count(*) as total from mascota;
END IF;

IF ve_opcion='opc_listar' then	
	select m.mascotaID as codigo, m.mascotaNombre as mascota,
    r.razaDescripcion as raza, 
    concat(p.personaNombres,' ',p.personaApellidos) as cliente,
    m.mascotaNumeroHistoriaClinica as hc,
	(case m.mascotaSexo when 0 then 'Hembra' else 'Macho' end) as sexo, 
	m.mascotaFechaNacimiento as nacimiento, 
	(case m.mascotaEstado when 1 then 'Activo' else 'Inactivo' end) as estado
    from mascota m
    inner join raza r on r.razaID = m.razaID
    inner join cliente c on c.clienteID = m.clienteID
    inner join persona p on p.personaID = c.personaID;
END IF;

IF ve_opcion = 'opc_contar_cliente' THEN
	SELECT count(*) as total from cliente;
END IF;

IF ve_opcion = 'opc_lista_cliente' THEN
	select c.clienteID as codigo, concat(p.personaNombres,' ',p.personaApellidos) as cliente from cliente c inner join persona p on p.personaID = c.personaID;
END IF;

END$$

CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_control_usuario`(IN `ve_opcion` VARCHAR(50), IN `ve_usuario` VARCHAR(50), IN `ve_password` VARCHAR(50))
    NO SQL
BEGIN
IF ve_opcion='opc_login_respuesta' THEN
	SET @CORRECTO = (SELECT COUNT(*) 
			FROM  usuario usu 
			WHERE 
				usu.usuarioLogin = ve_usuario AND
				usu.usuarioPassword = ve_password);
			IF @CORRECTO>0 THEN
				SELECT '1' AS 'respuesta';
			ELSE
				SELECT 'Usuario o clave incorrectos' AS 'respuesta';
			END IF;
END IF;

IF ve_opcion='opc_login_listar' THEN
	select u.personaID, u.usuarioLogin, v.veterinarioCodigo, p.personaDNI, v.sucursalID, s.sucursalNombre
    	from usuario u 
        join persona p on p.personaID = u.personaID
        join veterinario v on v.personaID = p.personaID
        join sucursal s on s.sucursalID = v.sucursalID
        where u.usuarioLogin = ve_usuario and u.usuarioPassword = ve_password;
END IF;
END$$


CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_combos`(IN `opcion` VARCHAR(100), IN `codigo` VARCHAR(100))
    NO SQL
BEGIN
	IF opcion = 'opc_combo_departamento' THEN
    	SELECT DISTINCT coddepartamento, departamento from ubigeo;
    END IF;
    IF opcion = 'opc_combo_provincia' THEN
    	select distinct codprovincia, provincia from ubigeo where coddepartamento = codigo;
    END IF;
    IF opcion = 'opc_combo_distrito' THEN
    	select distinct coddistrito, distrito from ubigeo where codprovincia = codigo;
    END IF;
    IF opcion = 'opc_listar_proveedores' THEN
    	SELECT PROV_nombre, PROV_tipo, PROV_ruc, PROV_telefono1, PROV_telefono2, PROV_direccion ,PROV_estado, PROV_codigo FROM proveedor;
    END IF;
END$$

